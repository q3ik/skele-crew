class TestProposedChangesFormat:
    def _four_hallucination_lessons(self) -> list[dict]:
        return [
            {
                "type": "entity",
                "name": f"lesson:2026-03-0{i}:hallucination-{i}",
                "entityType": "lesson",
                "observations": [
                    "category: hallucination",
                    f"summary: Hallucination incident {i}",
                ],
            }
            for i in range(1, 5)
        ]

    def test_proposed_changes_has_header(self) -> None:
        patterns = detect_patterns(self._four_hallucination_lessons())
        output = generate_proposed_changes(patterns, [], date="2026-03-17")
        # New format: per-proposal header with date
        assert "### Proposal:" in output
        assert "2026-03-17" in output

    def test_proposed_changes_has_generated_by_line(self) -> None:
        patterns = detect_patterns(self._four_hallucination_lessons())
        output = generate_proposed_changes(patterns, [], date="2026-03-17")
        assert "Generated by Improver Agent" in output

    def test_proposed_changes_has_skill_updates_section(self) -> None:
        patterns = detect_patterns(self._four_hallucination_lessons())
        output = generate_proposed_changes(patterns, [], date="2026-03-17")
        # New format uses per-proposal Type field instead of a Skill Updates section
        assert "**Type**: skill-change" in output

    def test_proposed_changes_includes_hallucination_skill_path(self) -> None:
        """Core acceptance criterion: output references skills/hallucination/SKILL.md."""
        patterns = detect_patterns(self._four_hallucination_lessons())
        output = generate_proposed_changes(patterns, [], date="2026-03-17")
        assert "skills/hallucination/SKILL.md" in output

    def test_proposed_changes_has_human_review_note(self) -> None:
        patterns = detect_patterns(self._four_hallucination_lessons())
        output = generate_proposed_changes(patterns, [], date="2026-03-17")
        assert "Human review required" in output

    def test_proposed_changes_no_patterns_shows_none(self) -> None:
        output = generate_proposed_changes([], [], date="2026-03-17")
        # With no patterns the output should indicate no proposals were generated
        assert "No proposals generated" in output
