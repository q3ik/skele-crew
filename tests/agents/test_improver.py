"""
Tests for the Improver agent file structure and pattern detection logic.

Validates the acceptance criteria from the issue:
- Agent file has required sections (monthly trigger, hard boundaries, pattern detection)
- Pattern detection: 3+ lessons in same category → create/update skills/[category]/SKILL.md
- New agent proposal: 5+ lessons in domain with no owner → propose new .agent.md
- PROPOSED_CHANGES.md output format matches template
- Test with 4 hallucination lessons → verify PROPOSED_CHANGES.md has a SKILL.md proposal
"""

from __future__ import annotations

import json
import re
from collections import Counter
from pathlib import Path

import pytest

# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

AGENTS_DIR = Path(__file__).resolve().parents[2] / ".github" / "agents"
IMPROVER_FILE = AGENTS_DIR / "improver.agent.md"

SKILL_THRESHOLD = 3
NEW_AGENT_THRESHOLD = 5


def _read_improver() -> str:
    return IMPROVER_FILE.read_text(encoding="utf-8")


def _parse_category(observation: str) -> str | None:
    """Extract category value from a lesson observation string.

    Matches observations starting with 'category:' (no space before the colon).
    The value after the colon is stripped of leading/trailing whitespace, so both
    'category: value' and 'category:value' are handled correctly.
    Returns None if the observation does not start with 'category:'.
    """
    if observation.startswith("category:"):
        return observation.split(":", 1)[1].strip()
    return None


def detect_patterns(lessons: list[dict]) -> list[tuple[str, int]]:
    """Mirror the Pattern Detection Algorithm described in improver.agent.md.

    Query all lesson entities. Group by category observation value.
    If any group has >= 3 entries, it qualifies for a SKILL.md proposal.

    Returns a list of (category, count) tuples for groups meeting the threshold.
    """
    counts: Counter[str] = Counter()
    for lesson in lessons:
        for obs in lesson.get("observations", []):
            category = _parse_category(obs)
            if category:
                counts[category] += 1
    return [(cat, count) for cat, count in counts.items() if count >= SKILL_THRESHOLD]


def detect_new_agent_candidates(lessons: list[dict]) -> list[tuple[str, int]]:
    """Return categories with >= 5 lessons (new agent threshold)."""
    counts: Counter[str] = Counter()
    for lesson in lessons:
        for obs in lesson.get("observations", []):
            category = _parse_category(obs)
            if category:
                counts[category] += 1
    return [(cat, count) for cat, count in counts.items() if count >= NEW_AGENT_THRESHOLD]


def generate_proposed_changes(
    skill_patterns: list[tuple[str, int]],
    new_agent_candidates: list[tuple[str, int]],
    date: str,
) -> str:
    """Produce per-proposal PROPOSED_CHANGES.md entries for a monthly cycle.

    Each qualifying pattern produces a separate ### Proposal: ... block following
    the per-proposal format defined in TEMPLATES.md (required fields: Type, Target
    file, Status, Rationale, Proposed change, Risk level, Protected sections affected).

    The blocks are appended under ## Pending Proposals in PROPOSED_CHANGES.md — they
    do NOT include the top-level heading or other top-level sections.
    """
    blocks: list[str] = []

    for cat, count in skill_patterns:
        blocks.append(
            f"### Proposal: {cat}-skill-update ({date})\n"
            "\n"
            "**Type**: skill-change\n"
            f"**Target file**: `.github/skills/{cat}/SKILL.md`\n"
            "**Status**: PENDING\n"
            "\n"
            "**Rationale**:\n"
            f"{count} lessons in category '{cat}' meet the skill update threshold"
            f" (≥{SKILL_THRESHOLD})."
            " Generated by Improver Agent after monthly lesson review.\n"
            "\n"
            "**Proposed change**:\n"
            f"Create or update `.github/skills/{cat}/SKILL.md` with observations"
            f" from the {count} '{cat}' lessons.\n"
            "\n"
            "**Risk level**: low\n"
            "**Protected sections affected**: none\n"
            "\n"
            "_Human review required — record decision in the Review Log._\n"
            "\n"
            "---"
        )

    for domain, count in new_agent_candidates:
        blocks.append(
            f"### Proposal: new-agent-{domain} ({date})\n"
            "\n"
            "**Type**: new-agent\n"
            f"**Target file**: `.github/agents/{domain}.agent.md`\n"
            "**Status**: PENDING\n"
            "\n"
            "**Rationale**:\n"
            f"Domain '{domain}' appeared {count} times with no existing agent owner,"
            " exceeding the new-agent threshold"
            f" (≥{NEW_AGENT_THRESHOLD})."
            " Generated by Improver Agent after monthly lesson review.\n"
            "\n"
            "**Proposed change**:\n"
            f"Draft a new `.github/agents/{domain}.agent.md` for the '{domain}' domain.\n"
            "\n"
            "**Risk level**: medium\n"
            "**Protected sections affected**: none\n"
            "\n"
            "_Human review required — record decision in the Review Log._\n"
            "\n"
            "---"
        )

    if not blocks:
        return (
            f"_(No proposals generated for this cycle ({date})"
            " — no category thresholds were met.)_"
        )

    return "\n\n".join(blocks)


