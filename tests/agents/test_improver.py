class TestPatternDetection:
    def _make_lesson(self, name: str, category: str, summary: str = "summary") -> dict:
        return {
            "type": "entity",
            "name": name,
            "entityType": "lesson",
            "observations": [
                f"category: {category}",
                f"summary: {summary}",
            ],
        }

    def test_no_lessons_returns_empty(self) -> None:
        assert detect_patterns([]) == []

    def test_two_lessons_same_category_below_threshold(self) -> None:
        lessons = [
            self._make_lesson("lesson:2026-03-01:a", "hallucination"),
            self._make_lesson("lesson:2026-03-02:b", "hallucination"),
        ]
        assert detect_patterns(lessons) == []

    def test_three_lessons_same_category_meets_threshold(self) -> None:
        lessons = [
            self._make_lesson("lesson:2026-03-01:a", "hallucination"),
            self._make_lesson("lesson:2026-03-02:b", "hallucination"),
            self._make_lesson("lesson:2026-03-03:c", "hallucination"),
        ]
        patterns = detect_patterns(lessons)
        assert len(patterns) == 1
        cat, count = patterns[0]
        assert cat == "hallucination"
        assert count == 3

    def test_four_hallucination_lessons_produce_skill_proposal(self) -> None:
        """Core acceptance criterion: 4 hallucination lessons → SKILL.md proposal."""
        lessons = [
            self._make_lesson(
                "lesson:2026-03-01:hallucination-1",
                "hallucination",
                "Agent made up a stat",
            ),
            self._make_lesson(
                "lesson:2026-03-02:hallucination-2",
                "hallucination",
                "Agent fabricated a source",
            ),
            self._make_lesson(
                "lesson:2026-03-03:hallucination-3",
                "hallucination",
                "Agent cited wrong law",
            ),
            self._make_lesson(
                "lesson:2026-03-04:hallucination-4",
                "hallucination",
                "Agent invented a deadline",
            ),
        ]
        patterns = detect_patterns(lessons)
        skill_proposals = [f"skills/{cat}/SKILL.md" for cat, _ in patterns]

        assert any("hallucination" in p for p in skill_proposals), (
            "4 hallucination lessons must trigger a skills/hallucination/SKILL.md proposal"
        )

    def test_mixed_categories_only_threshold_group_qualifies(self) -> None:
        lessons = [
            self._make_lesson("lesson:2026-03-01:a", "hallucination"),
            self._make_lesson("lesson:2026-03-02:b", "hallucination"),
            self._make_lesson("lesson:2026-03-03:c", "hallucination"),
            self._make_lesson("lesson:2026-03-04:d", "bug"),
            self._make_lesson("lesson:2026-03-05:e", "bug"),
        ]
        patterns = detect_patterns(lessons)
        categories = [cat for cat, _ in patterns]
        assert "hallucination" in categories
        assert "bug" not in categories

    def test_five_lessons_triggers_new_agent_candidate(self) -> None:
        lessons = [
            self._make_lesson(f"lesson:2026-03-0{i}:a", "wrong-domain")
            for i in range(1, 6)
        ]
        candidates = detect_new_agent_candidates(lessons)
        assert any(cat == "wrong-domain" for cat, _ in candidates)

    def test_four_lessons_does_not_trigger_new_agent_candidate(self) -> None:
        lessons = [
            self._make_lesson(f"lesson:2026-03-0{i}:a", "wrong-domain")
            for i in range(1, 5)
        ]
        candidates = detect_new_agent_candidates(lessons)
        assert not any(cat == "wrong-domain" for cat, _ in candidates)


