class TestImproverAgentFileStructure:
    @pytest.fixture(scope="class")
    def content(self) -> str:
        return _read_improver()

    def test_has_hard_boundaries_section(self, content: str) -> None:
        assert "## Hard Boundaries" in content

    def test_hard_boundaries_cannot_modify_agent_files(self, content: str) -> None:
        assert "CANNOT modify existing" in content and ".agent.md" in content

    def test_protected_financial_thresholds_present(self, content: str) -> None:
        assert "<!-- PROTECTED: financial-thresholds -->" in content

    def test_protected_legal_compliance_present(self, content: str) -> None:
        assert "<!-- PROTECTED: legal-compliance -->" in content

    def test_protected_auth_logic_present(self, content: str) -> None:
        assert "<!-- PROTECTED: auth-logic -->" in content

    def test_has_monthly_trigger_section(self, content: str) -> None:
        assert "## Monthly Trigger" in content

    def test_monthly_trigger_runs_on_first_of_month(self, content: str) -> None:
        # Ensure the 1st-of-month trigger is documented
        match = re.search(r"## Monthly Trigger.*?##", content, re.DOTALL)
        assert match, "Could not find ## Monthly Trigger section body"
        section = match.group(0)
        assert "1st of each month" in section

    def test_monthly_trigger_tracks_last_run_metric(self, content: str) -> None:
        match = re.search(r"## Monthly Trigger.*?##", content, re.DOTALL)
        assert match
        section = match.group(0)
        assert "metric:prompt:improver-monthly-cycle:last-run" in section

    def test_has_pattern_detection_algorithm_section(self, content: str) -> None:
        assert "## Pattern Detection Algorithm" in content

    def test_pattern_detection_mentions_skill_threshold(self, content: str) -> None:
        # The algorithm must specify "3" or "≥ 3" as the skill threshold
        match = re.search(r"## Pattern Detection Algorithm.*?##", content, re.DOTALL)
        assert match
        section = match.group(0)
        assert re.search(r"[≥>=]\s*3|3\s*or more", section), (
            "Pattern Detection Algorithm must specify a threshold of 3"
        )

    def test_pattern_detection_mentions_skills_path(self, content: str) -> None:
        match = re.search(r"## Pattern Detection Algorithm.*?##", content, re.DOTALL)
        assert match
        section = match.group(0)
        assert "skills/" in section

    def test_pattern_detection_mentions_new_agent_threshold(self, content: str) -> None:
        # New agent threshold (5+) must appear in the file
        assert re.search(r"5\s*or more|[≥>=]\s*5", content), (
            "Improver must specify 5+ lessons → new agent proposal threshold"
        )

    def test_has_self_improvement_cycle_section(self, content: str) -> None:
        assert "## Self-Improvement Cycle" in content

    def test_has_proposed_changes_format_section(self, content: str) -> None:
        assert "## PROPOSED_CHANGES.md Format" in content

    def test_proposed_changes_format_has_skill_updates(self, content: str) -> None:
        match = re.search(r"## PROPOSED_CHANGES\.md Format.*", content, re.DOTALL)
        assert match
        section = match.group(0)
        # New per-proposal format uses Type field with 'skill-change' value
        assert "skill-change" in section

    def test_proposed_changes_format_has_human_action_required(self, content: str) -> None:
        match = re.search(r"## PROPOSED_CHANGES\.md Format.*", content, re.DOTALL)
        assert match
        section = match.group(0)
        assert "Human review required" in section

    def test_has_trigger_table_section(self, content: str) -> None:
        assert "## Trigger Table" in content

    def test_has_consultation_heuristic_section(self, content: str) -> None:
        assert "## Consultation Heuristic" in content

    def test_consultation_heuristic_matches_canonical_text(self, content: str) -> None:
        canonical = (
            "If your output involves: money amounts, legal claims, auth systems, or cross-product"
            " changes → pause and request peer review before acting, even if no explicit trigger fires."
        )
        match = re.search(r"## Consultation Heuristic\s*\n([^\n]+)", content)
        assert match, "Could not find text after ## Consultation Heuristic"
        assert match.group(1).strip() == canonical


